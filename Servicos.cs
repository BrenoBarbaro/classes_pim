

public class Servicos {

	private int id_servicos;
	private string nomeServico;
	private string descricaoServico;
	private int valorServico;
	private string veiculo;
	public int getId_servicos() {
		return id_servicos;
	}
	public void setId_servicos(int id_servicos) {
		this.id_servicos = id_servicos;
	}
	public string getNomeServico() {
		return nomeServico;
	}
	public void setNomeServico(string nomeServico) {
		this.nomeServico = nomeServico;
	}
	public string getDescricaoServico() {
		return descricaoServico;
	}
	public void setDescricaoServico(string descricaoServico) {
		this.descricaoServico = descricaoServico;
	}
	public int getValorServico() {
		return valorServico;
	}
	public void setValorServico(int valorServico) {
		this.valorServico = valorServico;
	}
	public string getVeiculo() {
		return veiculo;
	}
	public void setVeiculo(string veiculo) {
		this.veiculo = veiculo;
	}
	
	
}
