
public class Combustivel {

	private String tipo;
	private int kmRodado;
	private double custoPorLitro;
	private int kmRodadosPorLitro;
	private double valorPorLitro;

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public int getKmRodado() {
		return kmRodado;
	}

	public void setKmRodado(int km) {
		this.kmRodado = km;
	}

	public double getCustoPorLitro() {
		return custoPorLitro;
	}

	public void setCustoPorLitro(double custoPorLitro) {
		this.custoPorLitro = custoPorLitro;
	}

	public int getKmRodadosPorLitro() {
		return kmRodadosPorLitro;
	}

	public void setKmRodadosPorLitro(int kmRodadosPorLitro) {
		this.kmRodadosPorLitro = kmRodadosPorLitro;
	}

	public double getValorPorLitro() {
		return valorPorLitro;
	}

	public void setValorPorLitro(double valorPorLitro) {
		this.valorPorLitro = valorPorLitro;
	}

}
