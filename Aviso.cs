
public class Aviso {                                             
    private int _id;                                             
    private string _assunto;                                     
    private string _texto;                                       
    private DateTime _SRdata;                                    
                                                                 
	public int get_id() {                                        
		return _id;                                              
	}                                                            
	public void set_id(int _id) {                                
		this._id = _id;                                          
	}                                                            
	public string get_assunto() {                                
		return _assunto;                                         
	}                                                            
	public void set_assunto(string _assunto) {                   
		this._assunto = _assunto;                                
	}                                                            
	public string get_texto() {                                  
		return _texto;                                           
	}                                                            
	public void set_texto(string _texto) {                       
		this._texto = _texto;                                    
	}                                                            
	public DateTime get_SRdata() {                               
		return _SRdata;                                          
	}                                                            
	public void set_SRdata(DateTime _SRdata) {                   
		this._SRdata = _SRdata;                                  
	}                                                            
                                                                 
}                                                                
                                                                 